package Projek;
public class NonPrimitf_1Dimensi {
    public static void main(String[] args) {
        String [] nama = {"Hijrana" , "Suci", "Riska", "Dwi", "Herlina", "Ayu", "Juli","Juni"};
        System.out.println("Panjang Array String:" + nama.length);
        
        //Menampilkan salah satu isi dari Artay
        System.out.println("Nama Pertama:" + nama[0]);
        
        //Menampilkan semua isi dari array
        for (int i = 0; i<nama.length; i++) {
            System.out.println("Nama:" + nama[i]);}
        
        
        
    }
    
    
}
